    public ArrayList<Spaarkaart> zoekSpaarkaarten(String kw)
    {
        ArrayList<Spaarkaart> zkList = new ArrayList<Spaarkaart>();
        for(Spaarkaart s : sList)
        {
            if(s.getSNaam().toLowerCase().startsWith(kw.toLowerCase()) || s.getVoornaam().toLowerCase().startsWith(kw.toLowerCase()) || s.getNaam().toLowerCase().startsWith(kw.toLowerCase()))
            {
                zkList.add(s);
            }
        }
        return zkList;
    }
    
    public void initTable(ArrayList<Spaarkaart> aList)
    {
        DefaultTableModel model = (DefaultTableModel) tblSpaarkaarten.getModel();
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--)
        {
            model.removeRow(i);
        }
        for(Spaarkaart s : sList)
        {
            model.addRow(new Object[]{s.getSId(), s.getSNaam(), s.getVoornaam(), s.getNaam()});
            this.curList = sList;
        }
    }
    
