    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String search = txtSearch.getText();
        System.out.println(search);
        if(search.equals(""))
        {
            initTable(sList);
        }
        else
        {
            ArrayList<Spaarkaart> zkList = zoekSpaarkaarten(search);
            initTable(zkList);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

    public void updateTable()
    {
        DefaultTableModel model = (DefaultTableModel) tblSpaarkaarten.getModel();
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--)
        {
            model.removeRow(i);
        }
        MySQL mySQL = new MySQL();
        sList = mySQL.getSpaarkaarten(ac.getID());
        curList = sList;
        model.setColumnIdentifiers(new String[]{"ID", "Naam", "Voornaam", "Accountnaam"});
        for(Spaarkaart s : sList)
        {
            model.addRow(new Object[]{s.getSId(), s.getSNaam(), s.getVoornaam(), s.getNaam()});
        }
    }
    
