    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(txtNaam.getText().equals("") || txtNaam.getText().length() > 128 )

        {
            JOptionPane.showMessageDialog(guiNW, "Naam (correct) invullen", "Naam (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else
             if(txtLogin.getText().equals("") || txtLogin.getText().length() > 128)
        {
            JOptionPane.showMessageDialog(guiNW, "Login (correct) invullen", "Login (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else
                  if(pwPass1.getText().equals("") || pwPass1.getText().length() < 6  || pwPass1.getText().length() > 32)
        {
            JOptionPane.showMessageDialog(guiNW, "Wachtwoord (correct) invullen", "Wachtwoord (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else
                       if(pwPass2.getText().equals(""))
        {
            JOptionPane.showMessageDialog(guiNW, "Herhaal wachtwoord", "Herhaal wachtwoord", JOptionPane.ERROR_MESSAGE);
        }
        else
                            if(!pwPass1.getText().equals(pwPass2.getText()))
        {
            JOptionPane.showMessageDialog(guiNW, "Wachtwoorden komen niet overeen", "Wachtwoorden komen niet overeen", JOptionPane.ERROR_MESSAGE);
        }
        else
                                {
                                    String naam = txtNaam.getText();
                                    String login = txtLogin.getText();
                                    String pass = pwPass1.getText();
                                    int id = wi.getWinkelID();
                                    int major = wi.getMajor();
                                    
                                    MySQL mySql = new MySQL();
                                    
                                    
                                        
                                    mySql.updateWinkel(id, naam, major, login, pass);
                                    clearTexts();
                                    JOptionPane.showMessageDialog(guiNW, "Winkel bewerkt", "Winkel bewerkt", JOptionPane.INFORMATION_MESSAGE);
                                     
                                    
                                    
                                    GuiWinkelBewerken guiNW = GuiWinkelBewerken.getInstance();
                                    setVisible(false);
                                    guiNW.setVisible(true);
                                    
                                    
                                    
                                    
                                    
                                }
    }//GEN-LAST:event_jButton2ActionPerformed

