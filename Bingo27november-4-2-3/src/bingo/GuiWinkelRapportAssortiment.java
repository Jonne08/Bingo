    private void btnExporterenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExporterenActionPerformed
        MySQL sql = new MySQL();
        ArrayList<Artikel> assortiment = new ArrayList<Artikel>();
        assortiment = sql.getArtikels(wi.getWinkelID());
        
        String str = "%11s\t%32s\t%16s\t%12s\t%12s\t%16s\t%11s\r\n";
        str = str.format(str, "ID", "Naam", "Barcode", "Punten gekr", "Punten kop", "Prijs", "Minimum");
       
        for(Artikel a : assortiment)
        {
            String st = "%11d\t%32s\t%16s\t%10.2f\t%10.2f\t%16s\t%11d\r\n";
            st = st.format(st, a.getID(), a.getNaam(), a.getBarcode(), a.getPuntenGekregen(), a.getPuntenKopen(), num.format(a.getPrijs()), a.getMinimum());
            str += st;
        }
        
        String file = wi.getNaam() + " Assortiment.txt";
        File f = new File(file);
        if(f.exists())
        {
            f.delete();
        }
        PrintWriter best = null;
        try
        {
            best = new PrintWriter(new FileOutputStream(file, true));
            best.append(str);
            best.flush();
            best.close();
        }
        catch(IOException e)
        {
            try
            {
                best.close();
            }
            catch(Exception ex)
            {
                 
            }
            e.printStackTrace();
        }  
       
        JOptionPane.showMessageDialog(guiWRA, "Rapport geëxporteerd.", "Rapport geëxporteerd.", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnExporterenActionPerformed

