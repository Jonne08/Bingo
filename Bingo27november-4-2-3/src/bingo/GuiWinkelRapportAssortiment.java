    public void updateTable()
    {
        MySQL sql = new MySQL();

        ArrayList<Artikel> assortiment = sql.getArtikels(wi.getWinkelID());

        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--)
        {
            model.removeRow(i);
        }
        for(Artikel a : assortiment)
        {
            model.addRow(new Object[]{a.getID(),a.getBarcode(), a.getNaam(), a.getPuntenGekregen(), a.getPuntenKopen(), num.format(a.getPrijs()), a.getMinimum()});
        }
    }
    
