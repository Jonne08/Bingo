    public void setWinkel(Winkel wi) 
    {
        this.wn = wi;
        MySQL sql = new MySQL();
        try 
        {
            fils = sql.getFilialen(wn.getWinkelID());
        } catch (SQLException ex) 
        {
        }
        wn.getNaam();
        curFils = fils;
        arts = sql.getArtikels(wn.getWinkelID());
        curArts = arts;
        major = sql.getMajor(wn.getWinkelID());

        setMajor();
    }

    public Winkel getWinkel()
    {
        return this.wn;
    }
    
