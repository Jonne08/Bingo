    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String search = txtSearch.getText();
        if(!search.equals(""))
        {
            lstFilialen.setVisible(true);
            curFils = zoekFilialen(search);
            initFList(curFils);
            lstArtikels.setVisible(true);
            curArts = zoekArtikels(search);
            initAList(curArts);
        }
        else
        {
            lstFilialen.setVisible(false);
            lstArtikels.setVisible(false);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

    public ArrayList<Filiaal> zoekFilialen(String search)
    {
        ArrayList<Filiaal> zkList = new ArrayList<Filiaal>();
        search = search.toLowerCase();
        for(Filiaal a : fils)
        {
            if(a.getNaam().toLowerCase().startsWith(search) || Integer.toString(a.getIdFiliaal()).startsWith(search))
            {
                zkList.add(a);
            }
        }
        return zkList;
    }    

    public ArrayList<Artikel> zoekArtikels(String search)
    {
        ArrayList<Artikel> zkList = new ArrayList<Artikel>();
        search = search.toLowerCase();
        for(Artikel a : arts)
        {
            if(a.getNaam().toLowerCase().startsWith(search) || a.getBarcode().startsWith(search) || Integer.toString(a.getID()).startsWith(search))
            {
                zkList.add(a);
            }
        }
        return zkList;
    }       
    
    public void initFList(ArrayList<Filiaal> fList)
    {
        DefaultListModel bmodel = (DefaultListModel) lstFilialen.getModel();
        bmodel.removeAllElements();
        for(Filiaal a : fList)
        {
            bmodel.addElement("[Filiaal " + a.getIdFiliaal() + "] " + a.getNaam());
            this.curFils = fList;
        }
        lstFilialen.setVisible(true);
    }

    public void initAList(ArrayList<Artikel> artList)
    {
        DefaultListModel bmodel = (DefaultListModel) lstArtikels.getModel();
        bmodel.removeAllElements();
        for(Artikel a : artList)
        {
            bmodel.addElement("[Artikel " + a.getID() + "] " + a.getNaam() + " (" + a.getBarcode() + ")");
            this.curArts = artList;
        }
        lstArtikels.setVisible(true);
    }    
    
