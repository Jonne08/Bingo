    public void updateTable()
    {
        MySQL sql = new MySQL();
        ArrayList<Verkopen> verkopen = sql.getVerkopenWinkelPunten(wi.getWinkelID());
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--)
        {
            model.removeRow(i);
        }
        for(Verkopen v : verkopen)
        {
            model.addRow(new Object[]{v.getId(), v.getNaam(), v.getDatum(), v.getHoev(), v.getPuntenkopen()});
            
        }
        model.addRow(new Object[]{"","","", "",sql.getVerkopenWinkelPuntenPK(wi.getWinkelID())});
    }

