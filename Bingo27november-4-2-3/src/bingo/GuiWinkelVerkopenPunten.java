    private void btnExporterenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExporterenActionPerformed
       MySQL sql = new MySQL();
        ArrayList<Verkopen> verkopen = sql.getVerkopenWinkelPunten(wi.getWinkelID());
           
        String str = "%11s\t%32s\t%10s\t%11s\t%12s\r\n";
        str = str.format(str, "ID", "Naam", "Datum", "Hoeveelheid", "Punten kop");

        double totaal = 0.0;
       
        for(Verkopen v : verkopen)
        {
            String stl = "";
            String st = "";
            stl = "%11d\t%32s\t%10s\t%11d\t%10.2f\r\n";
            st = st.format(stl, v.getId(), v.getNaam(), v.getDatum(), v.getHoev(), v.getPuntenkopen());
            str += st;
            totaal += v.getPuntenkopen();
        }
       
        str += "\t\t\t\t\t\t\t\t\t\t   " + "_______________" + "\r\n";
        String stb = "";
        stb = stb.format("\t\t\t\t\t\t\t\t\t\t\t%10.2f", totaal);
        str += stb;
        
        String file = wi.getNaam() + " VerkopenMetPunten.txt";
        File f = new File(file);
        if(f.exists())
        {
            f.delete();
        }
        PrintWriter best = null;
        try
        {
            best = new PrintWriter(new FileOutputStream(file, true));
            best.append(str);
            best.flush();
            best.close();
        }
        catch(IOException e)
        {
            try
            {
                best.close();
            }
            catch(Exception ex)
            {
                 
            }
            e.printStackTrace();
        }  
        JOptionPane.showMessageDialog(guiWRA, "Rapport geëxporteerd.", "Rapport geëxporteerd.", JOptionPane.INFORMATION_MESSAGE);        
    }//GEN-LAST:event_btnExporterenActionPerformed

