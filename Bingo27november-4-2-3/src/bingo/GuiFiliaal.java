    private void btnAddFiliaalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFiliaalActionPerformed
        Pattern tf1 = Pattern.compile("\\d{9}");
        Pattern tf2 = Pattern.compile("\\d{10}");
        Pattern tf3 = Pattern.compile("\\d{1,8}");
        Pattern tf4 = Pattern.compile("\\d{4}");
        
        Matcher matcher = tf1.matcher(txtTelefoon.getText());
        Matcher matcher2 = tf2.matcher(txtTelefoon.getText());
        Matcher matcher3 = tf3.matcher(txtNummer.getText());
        Matcher matcher4 = tf4.matcher(txtPostcode.getText());
        if(txtFiliaalnaam.getText().equals("") || txtFiliaalnaam.getText().length() > 128)
        {
            JOptionPane.showMessageDialog(guiF, "Filiaal naam (correct) invullen.", "Filiaal naam (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else if(txtStraat.getText().equals("") || txtStraat.getText().length() > 128)
        {
            JOptionPane.showMessageDialog(guiF, "Straat (correct) invullen.", "Straat (correct)invullen", JOptionPane.ERROR_MESSAGE);
        }
        else if(txtNummer.getText().equals("") || !matcher3.matches())
        {
            JOptionPane.showMessageDialog(guiF, "Nummer (correct) invullen.", "Nummer (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else if(txtPostcode.getText().equals("") || !matcher4.matches())
        {
            JOptionPane.showMessageDialog(guiF, "Postcode (correct) invullen.", "Postcode (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else if(txtStad.getText().equals("") || txtStad.getText().length() > 128)
        {
            JOptionPane.showMessageDialog(guiF, "Stad (correct) invullen.", "Stad (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else if(!matcher.matches() && !matcher2.matches())
        {
            JOptionPane.showMessageDialog(guiF, "Telefoon (correct) invullen.", "Telefoon (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else if(txtLogin.getText().equals("") || txtLogin.getText().length() > 32)
        {
            JOptionPane.showMessageDialog(guiF, "Login (correct) invullen.", "Login (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }                           
        else if(txtWachtwoord.getText().equals("") || txtWachtwoord.getText().length() > 32)
        {
            JOptionPane.showMessageDialog(guiF, "Wachtwoord (correct) invullen.", "Wachtwoord (correct) invullen", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String filiaalNaam = txtFiliaalnaam.getText();
            String straat = txtStraat.getText();
            String nummer = txtNummer.getText();
            String postcode = txtPostcode.getText();
            String stad = txtStad.getText();
            String telefoon = txtTelefoon.getText();
            String login = txtLogin.getText();
            String wachtwoord = txtWachtwoord.getText();
            int winkel = this.wn.getWinkelID();
            
            MySQL mySQL = new MySQL();
            try 
            {
                mySQL.addFiliaal(filiaalNaam, straat, nummer, postcode, stad, telefoon, winkel, login, wachtwoord);
                clearTexts();
                JOptionPane.showMessageDialog(guiF, "Filiaal toegevoegd", "Filiaal Toegevoegd", JOptionPane.INFORMATION_MESSAGE);
            } 
            catch (SQLException ex) 
            {
                //Logger.getLogger(GuiFiliaal.class.getName()).log(Level.SEVERE, null, ex);
            }
                       
            GuiWinkelIndex guiKI = GuiWinkelIndex.getInstance();
            //setVisible(false);
            //guiKI.setVisible(true);
        }            
    }//GEN-LAST:event_btnAddFiliaalActionPerformed

