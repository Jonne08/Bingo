    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(txtNaam.getText().equals(""))
        {
            JOptionPane.showMessageDialog(guiNW, "Naam invullen", "Naam invullen", JOptionPane.ERROR_MESSAGE);
        }
        else
             if(txtLogin.getText().equals(""))
        {
            JOptionPane.showMessageDialog(guiNW, "Login invullen", "Login invullen", JOptionPane.ERROR_MESSAGE);
        }
        else
                  if(pwPass1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(guiNW, "Wachtwoord invullen", "Wachtwoord invullen", JOptionPane.ERROR_MESSAGE);
        }
        else
                       if(pwPass2.getText().equals(""))
        {
            JOptionPane.showMessageDialog(guiNW, "Herhaal wachtwoord", "Herhaal wachtwoord", JOptionPane.ERROR_MESSAGE);
        }
        else
                            if(!pwPass1.getText().equals(pwPass2.getText()))
        {
            JOptionPane.showMessageDialog(guiNW, "Wachtwoorden komen niet overeen", "Wachtwoorden komen niet overeen", JOptionPane.ERROR_MESSAGE);
        }
        else
                                {
                                    String naam = txtNaam.getText();
                                    String login = txtLogin.getText();
                                    String pass = pwPass1.getText();
                                    
                                    MySQL mySql = new MySQL();
                                    
                                    try {
                                        
                                    mySql.addWinkel(naam, login, pass);
                                    clearTexts();
                                    JOptionPane.showMessageDialog(guiNW, "Winkel toegevoegd", "Winkel Toegevoegd", JOptionPane.INFORMATION_MESSAGE);
                                     } 
                                    catch(SQLException ex)
                                        {
                                            
                                        }
                                    
                                    GuiNieuweWinkel guiNW = GuiNieuweWinkel.getInstance();
                                    setVisible(false);
                                    guiNW.setVisible(true);
                                    
                                    
                                    
                                    
                                    
                                }
                                
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

